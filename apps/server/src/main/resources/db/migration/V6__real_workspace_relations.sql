CREATE TABLE parent_student (
  id BIGINT PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  parent_id BIGINT NOT NULL,
  student_id BIGINT NOT NULL,
  relation VARCHAR(32) NOT NULL DEFAULT 'PARENT',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  deleted_at DATETIME NULL,
  UNIQUE KEY uk_parent_student (parent_id, student_id),
  KEY idx_parent_student_tenant (tenant_id),
  CONSTRAINT fk_parent_student_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id),
  CONSTRAINT fk_parent_student_parent FOREIGN KEY (parent_id) REFERENCES parent_profile(id),
  CONSTRAINT fk_parent_student_student FOREIGN KEY (student_id) REFERENCES student_profile(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE class_teacher (
  id BIGINT PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  class_group_id BIGINT NOT NULL,
  teacher_user_id BIGINT NOT NULL,
  role_in_class VARCHAR(32) NOT NULL DEFAULT 'TEACHER',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  deleted_at DATETIME NULL,
  UNIQUE KEY uk_class_teacher (class_group_id, teacher_user_id),
  KEY idx_class_teacher_tenant (tenant_id),
  CONSTRAINT fk_class_teacher_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id),
  CONSTRAINT fk_class_teacher_class FOREIGN KEY (class_group_id) REFERENCES class_group(id),
  CONSTRAINT fk_class_teacher_user FOREIGN KEY (teacher_user_id) REFERENCES user_account(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE file_attachment (
  id BIGINT PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  owner_type VARCHAR(32) NOT NULL,
  owner_id BIGINT NOT NULL,
  uploader_user_id BIGINT NOT NULL,
  original_name VARCHAR(255) NOT NULL,
  storage_path VARCHAR(512) NOT NULL,
  content_type VARCHAR(128),
  file_size BIGINT NOT NULL DEFAULT 0,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  deleted_at DATETIME NULL,
  KEY idx_file_attachment_owner (tenant_id, owner_type, owner_id),
  CONSTRAINT fk_file_attachment_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id),
  CONSTRAINT fk_file_attachment_uploader FOREIGN KEY (uploader_user_id) REFERENCES user_account(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

